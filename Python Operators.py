#Python Arithmetic Operators
'''
Addition (+) : x + y
Subtraction (-) : x - y
Multiplication (*) : x * y
Divsion (/) : x / y
Modulus (%) : x % y
Exponentiation (**) : x ** y
Floor divsion (//) : x // y
'''

#Python Assignment Operators
'''
= : x = 5 : x = 5
+= : x += 3 : x = x + 3
-= : x -= 3 : x = x - 3
*= : x *= 3 : x = x * 3
/= : x /= 3 : x = x / 3
%= : x %= 3 : x = x % 3
//= : x //= 3 : x = x // 3
**= : x **= 3 : x = x ** 3
&= : x &= 3 : x = x & 3
|= : x |= 3 : x = x | 3
^= : x ^= 3 : x = x ^ 3
>>= : x >>= 3 : x = x >> 3
<<= : x <<= 3 : x = x << 3
'''

